# Write a Python program to convert a tuple to a string. 

def tuple_to_string(tup):
    return ''.join(tup)

# Example usage
string_tuple = ('K', 'e', 'y', 'u', 'r', ' ', 'C','h','a','m','p','a','w','a','t')
print(string_tuple)
result = tuple_to_string(string_tuple)
print("Converted string:", result)




