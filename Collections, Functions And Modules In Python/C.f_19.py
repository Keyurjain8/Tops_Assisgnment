# Write a Python program to create a tuple with different data types.  
# Creating a tuple with different data types
my_tuple = (1, 'Keyur', 37001.12, True, [1, 2, 3], {'key': 'value'}, (4, 5))

# Printing the tuple
print("Tuple with different data types:", my_tuple)

# Accessing elements to demonstrate the tuple
print("Integer element:", my_tuple[0])
print("String element:", my_tuple[1])
print("Float element:", my_tuple[2])
print("Boolean element:", my_tuple[3])
print("List element:", my_tuple[4])
print("Dictionary element:", my_tuple[5])
print("Tuple element:", my_tuple[6])
