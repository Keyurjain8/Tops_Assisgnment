# What is tuple? Difference between list and tuple. 
"""
Tuple:
A tuple is a built-in data type in Python that represents an ordered collection of elements. It is similar to a list,
but with one key difference: tuples are immutable, meaning that once a tuple is created, its elements cannot be modified, added, or removed. 
Tuples are defined by enclosing the elements in parentheses (), and they can contain elements of different data types.

List:
A list is a built-in data type in Python that represents an ordered collection of elements. Unlike tuples, lists are mutable, meaning their 
elements can be modified, added, or removed. Lists are defined by enclosing the elements in square brackets [], and they can also contain 
elements of different data types.
 """